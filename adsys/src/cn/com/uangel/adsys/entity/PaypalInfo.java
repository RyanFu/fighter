package cn.com.uangel.adsys.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 提现记录类
 * 
 * @author Tree
 * 
 */
public class PaypalInfo implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer id;
	private Integer mem_id;
	private Date apply_time;
	private String bod_number;
	private Double apply_amount=0.0;
	private Double allow_amount=0.0;
	private String state;
	private Date pay_date;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getMem_id() {
		return mem_id;
	}

	public void setMem_id(Integer mem_id) {
		this.mem_id = mem_id;
	}

	public Date getApply_time() {
		return apply_time;
	}

	public void setApply_time(Date apply_time) {
		this.apply_time = apply_time;
	}

	public String getBod_number() {
		return bod_number;
	}

	public void setBod_number(String bod_number) {
		this.bod_number = bod_number;
	}

	public Double getApply_amount() {
		return apply_amount;
	}

	public void setApply_amount(Double apply_amount) {
		this.apply_amount = apply_amount;
	}

	public Double getAllow_amount() {
		return allow_amount;
	}

	public void setAllow_amount(Double allow_amount) {
		this.allow_amount = allow_amount;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public Date getPay_date() {
		return pay_date;
	}

	public void setPay_date(Date pay_date) {
		this.pay_date = pay_date;
	}

}
