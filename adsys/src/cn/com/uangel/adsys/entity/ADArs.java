package cn.com.uangel.adsys.entity;

import java.io.Serializable;

/**
 * 广告_运营商
 * 
 * @author Tree
 * 
 */
public class ADArs implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer id;
	private Integer ad_id;
	private String ars_name;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getAd_id() {
		return ad_id;
	}

	public void setAd_id(Integer ad_id) {
		this.ad_id = ad_id;
	}

	public String getArs_name() {
		return ars_name;
	}

	public void setArs_name(String ars_name) {
		this.ars_name = ars_name;
	}

	@Override
	public String toString() {
		return "id: " + id + ", ad_id: " + ad_id + ",ars_name: " + ars_name;
	}
}
