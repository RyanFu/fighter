package cn.com.uangel.adsys.entity;

import java.io.Serializable;

/**
 * 统计信息类
 * 
 * @author Tree
 * 
 */
public class AppStatInfo implements Serializable {
	private static final long serialVersionUID = 1L;

	private String date;
	private Integer request_times;
	private Integer show_times;
	private Integer click_times;
	private Double cpm_commision;
	private Double cpc_commision;
	private Double total_income;// 收入金额

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public Integer getRequest_times() {
		return request_times;
	}

	public void setRequest_times(Integer request_times) {
		this.request_times = request_times;
	}

	public Integer getShow_times() {
		return show_times;
	}

	public void setShow_times(Integer show_times) {
		this.show_times = show_times;
	}

	public Integer getClick_times() {
		return click_times;
	}

	public void setClick_times(Integer click_times) {
		this.click_times = click_times;
	}

	public Double getCpm_commision() {
		return cpm_commision;
	}

	public void setCpm_commision(Double cpm_commision) {
		this.cpm_commision = cpm_commision;
	}

	public Double getCpc_commision() {
		return cpc_commision;
	}

	public void setCpc_commision(Double cpc_commision) {
		this.cpc_commision = cpc_commision;
	}

	public Double getTotal_income() {
		return total_income;
	}

	public void setTotal_income(Double total_income) {
		this.total_income = total_income;
	}

}
